    Console.Write("Введите КОРРЕКТНОЕ число дней (от 1 до 100): ");
n = Convert.ToInt32(Console.ReadLine());
}
int[] arr = new int[n];
for(int i = 0; i < arr.Length; i++){
 arr[i] = new Random().Next(1, 32);
}
Console.WriteLine($"[{String.Join(", ", arr)}]");

int even = 0;
int odd = 0;

for(int i = 0; i < arr.Length; i++){
    if(arr[i] % 2 == 0){
        even++;
        }
    else{
        odd++;
    }
}

int[] evens = new int[even];
int[] odds = new int[odd];

    int i_ev = 0;
    int i_od = 0;

for(int i = 0; i < arr.Length; i++){

    if(arr[i] % 2 == 0){
        evens[i_ev] = arr[i];
        i_ev++;
        }
    else{
        odds[i_od] = arr[i];
        i_od++;
    }
}
Console.WriteLine($"Чётные: [{String.Join(", ", evens)}]");
